#ifndef TIME_H_
#define TIME_H_

#include "hardware.h"

/************************************************************************/
/* Represents an accurate moment for internal timekeeping.              */
/************************************************************************/
typedef struct TIMEVAL_s {
	WORD secs;
	WORD parts;
} TIMEVAL;

/************************************************************************/
/* Returns the time since system start in ms.                           */
/************************************************************************/
DWORD CurrentTime();

/************************************************************************/
/* Sleeps s seconds.                                                    */
/************************************************************************/
void Sleep(WORD s);

/************************************************************************/
/* Sleeps ms milliseconds.                                              */
/************************************************************************/
void Msleep(WORD ms);

/************************************************************************/
/* Sleep us microseconds ACTIVE.                                        */
/************************************************************************/
void Usleep(WORD us);

#endif /* TIME_H_ */